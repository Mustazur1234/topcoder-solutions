int SubdividedSlimes::needCut(int S, int M) {

for(int k=1;k<S;k++){
        int number=S;
        int cost=0;
      for(int j=k;j>=1;j--){
          if(number>=1){
        int first=number/(j+1);// j+1 for s-1 is s;
        int second=number-first;
    cost+=first*second;
      number=number-first;
      }
}
if(cost>=M)
    return k;
}
return -1;
}

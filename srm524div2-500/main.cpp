class MagicDiamonds {
public:
	long long minimalTransfer(long long);
};
bool checkprime(ll n){
    int co=0;
    for(int i=1;i*i<=n;i++){
        if(n%i==0){
            if(i*i==n) co++;
            else
                co+=2;
        }
    }
    if(co==2)
        return true;
    else
        return false;
    }



long long MagicDiamonds::minimalTransfer(long long n) {

	if(checkprime(n)==false)
        return 1;
    if (n==3)
        return 3;
    return 2;// all other caese
}

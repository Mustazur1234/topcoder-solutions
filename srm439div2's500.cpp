class PouringWater {
public:
	int getMinBottles(int, int);
};
/*1 1 1 1 1 1 1 =7
   2 2 2 1
    4 2 1 i.e 0111 so minimum of three*/
/* 1 1 1 1 1 1 1 1 
    2   2   2  2 
	  4      4
	      8  i.e 1000 so minimum of one */    
    
    
int PouringWater::getMinBottles(int N, int K) {
	int n=N;
	while(__builtin_popcount(n) > K){
		n++;
	}
	return n-N;// 
}

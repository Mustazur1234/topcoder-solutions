	int a[N];// 0 1 2 3 FOR N=4
	for(int i=1;i<=N;i++){
        a[i-1]=i;
	}
	ll p=0;

	do{
            int magic=0;
        for(int i=0;i<N;i++){
            magic+=max(i+1,a[i]);
        }
        if(magic>=K)
        p++;
	}
	while(next_permutation(a,a+N));
	ll res=1LL;

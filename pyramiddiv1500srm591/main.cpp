typedef long long ll;

class PyramidSequences {
public:
	long long distinctPairs(int, int);
};

long long PyramidSequences::distinctPairs(int N, int M) {
    ll sum1=0;ll sum2=0;
	// size of square side
	ll g=__gcd(N-1,M-1);
	ll x=(N-1)/g;// no of squares on row side
	ll y=(M-1)/g;// column side
	// number of marked points inside square
    sum1=x*y*(g-1);
	// points on periphery of each square
	sum2=((x+1)*(y+1)+1)/2;
	return sum1+sum2;

}

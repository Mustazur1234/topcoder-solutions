typedef long long ll;

class Undiv2 {
public:
	long long getsum(int);
};
ll lcm(ll a,ll b){
    return (a*(b/__gcd(a,b)));
    }
long long Undiv2::getsum(int n) {
    ll ans=0;
    for(ll i=1;i<=48;i++){//second smallest
        for(ll j=1;j<i;j++){// first smallest so has to be less than i
           // for each pair (i,j)
           ll val=1;// val is the lowest common multiple
        for(ll k=1;k<i;k++){
            // if i=10 and j=3 ,then lcm=lcm of (1,2,4.5.6.7.8,9) as we hv to skip i and j for each i,j pair
            if(k!=j)
                val=lcm(val,k);
        }
        ll cal=(n/val)-(n/lcm(val,i))-(n/lcm(val,j))+(n/lcm(val,lcm(i,j)));
        ans+=cal*i;
        }

    }

    return ans;
}

typedef long long ll;

class LastDigit {
public:
	long long findX(long long);
};
ll f(ll n){
	ll sum=0;
	while(n>0){
		sum+=n;
		n=n/10;
	}
	return sum;
}
long long LastDigit::findX(long long S) {
ll low=1;
ll high=10000000000*10000000000;//10^18
ll target=S;
while(low<=high){
	ll mid=low+(high-low)/2;
	if(f(mid)==target)
	return mid;
	else if(f(mid)<target)
	low=mid+1;
	else
	high=mid-1;
	
}

return -1;

	
}


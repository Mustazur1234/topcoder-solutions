class Drbalance {
public:
	int lesscng(string, int);
};
// this function calculates the negativity of a string or how many prefixes of string have a total negative value
int calculate(string s){
 int x=0;int ans=0;
 for(int i=0;i<s.length();i++)
{
    if(s[i]=='-')
    {
        x--;
    }
    else if(s[i]=='+'){
        x++;
    }
    if(x<0)
        ans++;// string is net negative
}
return ans;

}
int Drbalance::lesscng(string s, int k) {
    int total=0;
	while(calculate(s)>k){
       // negativity is still more we are going to make changes
       for(int i=0;i<s.length();i++){
        if(s[i]=='-'){
            s[i]='+';
            total++;
            break;// change and see
        }
       }

	}
	return total;



}


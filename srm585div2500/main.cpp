typedef long long ll;

class TrafficCongestionDivTwo {
public:
	long long theMinCars(int);
};
ll f[100];
long long TrafficCongestionDivTwo::theMinCars(int treeHeight) {
f[1]=f[0]=1;
for(int t=2;t<=treeHeight;t++){
    f[t]=1;
    for(int i=0;i<=t-2;i++){
        f[t]+=2*f[i];
    }
}


return f[treeHeight];
}


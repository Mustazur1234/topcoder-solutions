class UpDownHiking {
public:
	int maxHeight(int, int, int);
};

int UpDownHiking::maxHeight(int N, int A, int B) {
	int maxii;
	int ans=0;
	for(int i=1;i<=N;i++){
	  maxii=min(i*A,(N-i)*B);// for every ith day calculate maxii
	  	ans=max(ans,maxii);// return the maxzimum of all maxii
	}
	return ans;
}


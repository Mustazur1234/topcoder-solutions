class PrisonCells {
public:
	int scatter(int, int, int);
};

int PrisonCells::scatter(int m, int n, int nr) {
		if(nr>=m*n)// total number of prisoners =m*n or >
        return 1;

        // maximum number of black or white cell
        int bw=(m*n+2-1)/2;//(a+b-1)/b
        if(nr>bw)
            return 1;
             if(nr>4)
            return 2;

    int ans=(2*n+2*m-4)/nr;
    return ans;
}


string InfiniteString::equal(string s, string t) {
    string a=s;
    string b=t;
        while(a.length()!=b.length()){
            if(a.length()<b.length())
                a+=s;
            else
                b+=t;
        }
        if(a==b)
        return "Equal";
        else
            return "Not equal";

    }




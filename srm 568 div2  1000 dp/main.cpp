class ShuffleSort {
public:
	double shuffle(vector <int>);
};

double f[100];
double ShuffleSort::shuffle(vector <int> cards) {
    sort(cards.rbegin(),cards.rend());
    f[1]=1;
    double t=1;
    for(int n=2;n<=cards.size();n++){
        if(cards[n-1]==cards[n-2]){
            t=t+1.0;
        }
        else{
            t=1.0;
        }
        f[n]=n/t+(f[n-1]-1);
    }
	return f[cards.size()];

	}

class OddEvenTree {
public:
	vector <int> getTree(vector <string>);
};

vector <int> OddEvenTree::getTree(vector <string> x) {
	int len=x.size();
	vector<int> v;
v.push_back(-1);
	// checking if tree can be built or not
	for(int i=0;i<len;i++)
	{
	    for(int j=0;j<len;j++){
            if(x[i][j]!=x[j][i]){// (2,1)!=(1,2) and so on asymmetric
                return v;
            }
            if(x[0][i]!=x[0][j]&& x[i][j]!='O')// parities different but distance between node is not odd
            {
                return v;
            }
            if(x[0][i]==x[0][j]&& x[i][j]!='E')// parities same but distance not even
            {
                return v;
            }
	    }
	}
	vector <int> edges;
	int odd=-1;
	for(int i=1;i<len;i++){
        if(x[0][i]=='O')// ODD DISTANCE FROM NODE SO ADD IT TO ROOT NODE
        {
            edges.push_back(0);edges.push_back(i);
            odd=i;// ith node is the odd node to which we connect our even vertices

        }
	}
	if(odd!=-1){
        for(int i=1;i<len;i++){
            if(x[0][i]=='E'){
                edges.push_back(odd);edges.push_back(i);
            }
        }
	}
	else{
        return v;
	}


	return edges;

}


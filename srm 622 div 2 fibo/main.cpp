vector <int> v;
int FibonacciDiv2::find(int N) {
	  int ans=100000000;
     int a=0;
    int b=1;
	int i=0;
	while(i<50){
          int   c=a+b;//c=0+1=1 // c=2
          if(c>0)
        v.push_back(c);
    a=b;//a=1; //a=1;
    b=c;// b=1 // b=2
    i++;
	}
	for(int i=0;i<v.size();i++){
            //cout<<v[i]<<" ";
  //  cout<<endl;
      ans=min(abs(v[i]-N),ans)   ;

	}
	return ans;
}

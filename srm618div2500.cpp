bool possible(string s,string t){
	int len=t.size();
	int j=0;
	int match=0;
	for(int i=0;i<s.size();i++){
		if(s[i]==t[j]){
			j++;
			match=match+1;
		}	
	}
	if(match==len)
	return true;
	return false;
}
string LongWordsDiv2::find(string word) {
	// generate all xyxy
	for (char i='A';i<='Z';i++){
		for(char j='A';j<='Z';j++){
			string s=string(1,i)+string(1,j)+string(1,i)+string(1,j);//xyxy
			bool ans=possible(word,s);
			if(ans==true)
			return  "Dislikes";

			
		}
	
	}
  int l=word.size();
  for(int i=0;i<l;i++){
  	if(word[i]==word[i+1] && i!=l-1){
  		return "Dislikes";

	  }
  }
  return "Likes";

		}


